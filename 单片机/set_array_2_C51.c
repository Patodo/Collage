void LED_T0() {
	led = 1;
	_nop_();
	led = 0;
	_nop_();_nop_();_nop_();_nop_();_nop_();_nop_();
}
void sendHELP(unsigned char RGB) {
	unsigned char i = 8;
	while(i--) {
		if(RGB&0x80) 		LED_T1();
		else 			LED_T0();
		RGB <<= 1;
	}
}
void sendRGB(unsigned char j) {
	/*sendHELP(buf[i][1]);//G
	sendHELP(buf[i][0]);//R
	sendHELP(buf[i][2]);//B*/
	unsigned char i, k, l;
	unsigned char RGB, RGB1, RGB2;
	k = 8;RGB = buf[j][0];
	i = 8;RGB1 = buf[j][1];
	l = 8;RGB2 = buf[j][2];
	while(i--) {
		if(RGB1&0x80) 		LED_T1();
		else 			LED_T0();
		RGB1 <<= 1;
	}
	while(k--) {
		if(RGB&0x80) 		LED_T1();
		else 			LED_T0();
		RGB <<= 1;
	}
	while(l--) {
		if(RGB2&0x80) 		LED_T1();
		else 			LED_T0();
		RGB2 <<= 1;
	}
}
void sendAll() {
	unsigned int i;
	for(i=0; i<N; ++i) sendRGB(i);
}
